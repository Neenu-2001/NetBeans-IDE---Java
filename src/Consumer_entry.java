    private void insertbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbuttActionPerformed
        // TODO add your handling code here:
        String consumer_no = conno.getText();
        String consumer_name = conname.getText();
        String telephone = tel.getText();
        String transformer_no = trans.getSelectedItem().toString();
        String meter_no= meter.getSelectedItem().toString();
        String purpose = purp.getSelectedItem().toString();
        String deposit = dep.getText();
        String loads =loa.getText();
        String elec_board_id =eidtxt.getText();
        String elec_board_name =ename.getText();
        String elec_board_address =eadd.getText();
        String location_id = locid.getSelectedItem().toString();
        String incharge = inch.getSelectedItem().toString();
        
        try {
            
            PreparedStatement ps = cons.Getconnection().prepareStatement("insert into consumer_master (consumer_no,consumer_name,telephone,transformer_no,meter_no,purpose,deposit,loads,elec_board_id,elec_board_name,elec_board_address,location_id,incharge)values(?,?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1, consumer_no);
            ps.setString(2, consumer_name);
            ps.setString(3, telephone);
            ps.setString(4, transformer_no);
            ps.setString(5,  meter_no);
            ps.setString(6, purpose);            
            ps.setString(7, deposit);
            ps.setString(8, loads);
            ps.setString(9, elec_board_id);
            ps.setString(10, elec_board_name); 
            ps.setString(11, elec_board_address); 
            ps.setString(12, location_id );
            ps.setString(13, incharge);
            ps.executeUpdate ();
            JOptionPane.showMessageDialog(null, "Data Inserted Successfully");
            ref_data();
            conno.setText("");
            conname.setText("");
            tel.setText("");
            trans.setSelectedItem(0);
            meter.setSelectedItem(0);
            purp.setSelectedItem(0);
            dep.setText("");
            loa.setText("");
            eidtxt.setText("");
            ename.setText("");
            eadd.setText("");
            locid.setSelectedItem(0);
            inch.setSelectedItem(0);
           
        }
     catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to insert data: " + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_insertbuttActionPerformed

