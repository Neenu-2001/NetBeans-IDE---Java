   private void disp_board_details() {
       String eid=eidtxt.getText();
       try
       {
           PreparedStatement ps =cons.Getconnection().prepareStatement("select elec_board_name,elec_board_address" + " from electricity_board_master where elec_board_id=?");
           ps.setString(1,eid);
           consrst=ps.executeQuery();
           if(consrst.next() ){
               ename.setText(consrst.getString("elec_board_name"));
               eadd.setText(consrst.getString("elec_board_address"));
         }
       }
           catch(Exception ex){
                 
                   Logger.getLogger(Consumer_entry.class.getName()).log(Level.SEVERE, null, ex);
                   }
       }
    
    private void ref_data() {
        try {
         model.setRowCount(0);
        consrst = consstmt.executeQuery("select * from consumer_master");
        while(consrst.next ()) {
        model.addRow(new Object[]
        {consrst.getString("consumer_no"),consrst.getString("consumer_name"),consrst.getString("telephone"),consrst.getString("transformer_no"),consrst.getString("meter_no"),consrst.getString("purpose"),consrst.getString("deposit"),consrst.getString("loads"),consrst.getString("elec_board_id"),consrst.getString("elec_board_name"),consrst.getString("elec_board_address"),consrst.getString("location_id"),consrst.getString("incharge")});
         }
        }
         catch (Exception e){
             System.err.println(e);
        }
        
    }
