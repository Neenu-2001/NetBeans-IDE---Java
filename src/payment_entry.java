    private void ref_data() {
       try {
        model.setRowCount(0);
        payrst = paystmt.executeQuery("select * from payment_master");
        while(payrst.next ()) {
        model.addRow(new Object[]
        {payrst.getString("receipt_no"),payrst.getString("consumer_no"),payrst.getString("consumer_name"),payrst.getString("due_date"),payrst.getString("bill_number"),payrst.getString("bill_amount"),payrst.getString("payment_mode"),payrst.getString("description"),payrst.getString("paid_amount"),payrst.getString("paid_date"),payrst.getString("balance_amount")});
         }
        }
         catch (Exception e){
             System.err.println(e);
         }
      
  } 
      
    
