    private void deletebuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebuttActionPerformed
        // TODO add your handling code here:
        
        String dsql  = "Delete from payment_master where receipt_no = '" + rn.getText() + "'" ;
        try {
            if (JOptionPane.showConfirmDialog(this, "Attention You are going to delete payment information","Delete transformer details",JOptionPane.YES_NO_OPTION)== JOptionPane.OK_OPTION) {
            if (rn.getText().length() !=0){
            paystmt.executeUpdate(dsql);
            ref_data();
           rn.setText("");
           cn.setText("");
           cna.setText("");
           pd.setDate(null);
           bno.setText("");
           bamt.setText("");
           pcomb.setSelectedItem(0);
           desc.setText("");
           pamt.setText("");
           dd.setDate(null);
           blamt.setText("");
            JOptionPane.showMessageDialog(this,"Record Deleted Successfully");
            }
            else {
                JOptionPane.showMessageDialog(this,"Enter Id value to delete..");
    
                 }    
              }
        }
        catch (Exception e) { 
           JOptionPane.showMessageDialog(this, "Unable to Delete:" + e.getLocalizedMessage());
        }  
    }//GEN-LAST:event_deletebuttActionPerformed
