   private void disp_consumers_details() {
       String con =cn.getText();
       try
       {
           PreparedStatement ps =pay.Getconnection().prepareStatement("select consumer_name,due_date,bill_number,bill_amount " + " from consumption_master where consumer_no=?");
           ps.setString(1,con);
           payrst=ps.executeQuery();
           if(payrst.next() ){
               cna.setText(payrst.getString("consumer_name"));
               pd.setDate(payrst.getDate("due_date"));
               bno.setText(payrst.getString("bill_number"));
               bamt.setText(payrst.getString("bill_amount"));
               
         }
       }
           catch(Exception ex){
                 
                   Logger.getLogger(Consumer_entry.class.getName()).log(Level.SEVERE, null, ex);
                   }
       
      
    }//GEN-LAST:event_cnKeyPressed

