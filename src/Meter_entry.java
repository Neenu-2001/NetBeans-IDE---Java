    private void filldeptCombo() {
             try{
            PreparedStatement ps = met.Getconnection().prepareStatement("select elec_board_id from electricity_board_master");
            metrst = ps.executeQuery();
            while(metrst.next()){
                eidcomb.addItem(metrst.getString(1));
            }
        }
        catch (Exception e){
            System.err.println(e);
        }
 }
   
 private void ref_data() {
     try {
         model.setRowCount(0);
        metrst = metstmt.executeQuery("select * from meter_master");
        while(metrst.next ()) {
        model.addRow(new Object[]
        {metrst.getString("meter_no"),metrst.getString("elec_board_id"),metrst.getString("meter_type"),metrst.getString("meter_rent")});
         }
        }
         catch (Exception e){
             System.err.println(e);
        }
}    
    

