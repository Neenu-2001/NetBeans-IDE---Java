    private void insertbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbuttActionPerformed
        // TODO add your handling code here:
        String meter_no = mno.getText();
        String elec_board_id = eidcomb.getSelectedItem().toString();
        String meter_type = mtype.getText();
        String meter_rent = mrent.getText();
        try {
            
            PreparedStatement ps = met.Getconnection().prepareStatement("insert into meter_master (meter_no,elec_board_id,meter_type,meter_rent) values (?,?,?,?)");
            ps.setString(1, meter_no);
            ps.setString(2, elec_board_id);
            ps.setString(3, meter_type);
            ps.setString(4, meter_rent);
            ps.executeUpdate ();
            JOptionPane.showMessageDialog (null, "Data Inserted Successfully");
            ref_data();
            mno.setText("");
            eidcomb.setSelectedItem(0);
            mtype.setText("");
            mrent.setText("");
        }
        catch (Exception e){           
            JOptionPane.showMessageDialog (null, "Unable to insert data: " + e.getLocalizedMessage());
        
        }
    }//GEN-LAST:event_insertbuttActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try{ 
            PreparedStatement ps;
            ps=met.Getconnection().prepareStatement("update meter_master set elec_board_id=?,meter_type=?,meter_rent=? where meter_no=?");
            ps.setString(1,eidcomb.getSelectedItem().toString());
            ps.setString(2,mtype.getText());
            ps.setString(3,mrent.getText());
            ps.setString(4,mno.getText());

        if (JOptionPane.showConfirmDialog (null,"Confirm Modification","modification",JOptionPane.YES_NO_OPTION)== JOptionPane.OK_OPTION) {
            ps.executeUpdate();
            ref_data();
            mno.setText("");
            eidcomb.setSelectedItem(0);
            mtype.setText("");
            mrent.setText("");
            JOptionPane.showMessageDialog(null, "Data Updated Successfully");
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Unable to update :" + e.getLocalizedMessage());
            }        
        
    }//GEN-LAST:event_updateActionPerformed

