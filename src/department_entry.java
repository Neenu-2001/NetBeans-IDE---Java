   private void disp_location_details() {
       String loc =txtlocid.getText();
       try
       {
           PreparedStatement ps =dept.Getconnection().prepareStatement("select location_name " + " from location_master where location_id=?");
           ps.setString(1,loc);
           deptrst=ps.executeQuery();
           if(deptrst.next() ){
               txtdept.setText(deptrst.getString("location_name"));
              
         }
       }
           catch(Exception ex){
                 
                   Logger.getLogger(Consumer_entry.class.getName()).log(Level.SEVERE, null, ex);
                   }
       
      
    }//GEN-LAST:event_txtlocidKeyPressed

      private void ref_data() {
     try {
        model.setRowCount(0);
        deptrst = deptstmt.executeQuery("select * from department_master");
        while(deptrst.next ()) {
        model.addRow(new Object[]
        {deptrst.getString("Department_id"),deptrst.getString("Department_name"),deptrst.getString("Dept_manager_id"),deptrst.getString("Location_id"),deptrst.getString("Dept_location")});
         }
        }
         catch (Exception e){
             System.err.println(e);
         }
      }

