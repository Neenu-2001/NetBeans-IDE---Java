    private void insertbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbuttActionPerformed
        String Department_id = dptidtxt.getText();
        String Department_name = deptnametxt.getText();
        String Dept_manager_id = deptmantxt.getText();
        String Location_id = txtlocid.getText();
        String Dept_location = txtdept.getText();
        try {
            
            PreparedStatement ps = dept.Getconnection().prepareStatement("insert into department_master (Department_id,Department_name,Dept_manager_id,Location_id,Dept_location) values (?,?,?,?,?)");
            ps.setString(1,Department_id  );
            ps.setString(2,Department_name );
            ps.setString(3,Dept_manager_id  );
            ps.setString(4,Location_id);
            ps.setString(5,Dept_location);
            ps.executeUpdate ();
           JOptionPane.showMessageDialog (null, "Data Inserted Successfully");
        ref_data();   
        dptidtxt.setText("");
        deptnametxt.setText("");
        deptmantxt.setText("");
        txtlocid.setText("");
        txtdept.setText("");
        }
        catch (Exception e){           
            JOptionPane.showMessageDialog (null, "Unable to insert data: " + e.getLocalizedMessage());
        }
   
    }//GEN-LAST:event_insertbuttActionPerformed

