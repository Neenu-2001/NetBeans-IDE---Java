    private void updatebuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttActionPerformed
    try{ 
            PreparedStatement ps;
            ps=dept.Getconnection().prepareStatement("update department_master set Department_name=?,Dept_manager_id =?,Location_id=?,Dept_location=? where Department_id=?");
            ps.setString(1,deptnametxt.getText());
            ps.setString(2,deptmantxt.getText());
            ps.setString(3,txtlocid.getText());
            ps.setString(4,txtdept.getText());
            ps.setString(5,dptidtxt.getText());
            
        if (JOptionPane.showConfirmDialog (null,"Confirm Modification","modification",JOptionPane.YES_NO_OPTION)== JOptionPane.OK_OPTION) {
            ps.executeUpdate();
            ref_data();
            deptnametxt.setText("");
            deptmantxt.setText("");
            txtlocid.setText("");
            txtdept.setText("");
            dptidtxt.setText("");
            JOptionPane.showMessageDialog(null, "Data Updated Successfully");
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Unable to update :" + e.getLocalizedMessage());
            }        
                                                   // TODO add your handling code here:
    }//GEN-LAST:event_updatebuttActionPerformed

