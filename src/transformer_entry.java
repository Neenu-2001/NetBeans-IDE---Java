    private void updatebuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttActionPerformed
     
                          // TODO add your handling code here:
         try{ 
            PreparedStatement ps;
            ps=tran.Getconnection().prepareStatement("update transformer_master set transformer=?,transformer_type=?,capacity=?,elec_board_id=?,elec_board_name=? where transformer_no=?");
            ps.setString(1,ttxt.getText());
            ps.setString(2,ttype.getText());
            ps.setString(3,captxt.getText());
            ps.setString(4,eleid.getText());
            ps.setString(5,elename.getText());
            ps.setString(6,tnotxt.getText());
        if (JOptionPane.showConfirmDialog (null,"Confirm Modification","modification",JOptionPane.YES_NO_OPTION)== JOptionPane.OK_OPTION) {
            ps.executeUpdate();
         ref_data();
        tnotxt.setText("");
        ttxt.setText("");
        ttype.setText("");
        captxt.setText("");
        eleid.setText("");
        elename.setText("");
            JOptionPane.showMessageDialog(null, "Data Updated Successfully");
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Unable to update :" + e.getLocalizedMessage());
            }        
                                    
    }//GEN-LAST:event_updatebuttActionPerformed

    private void insertbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbuttActionPerformed
        // TODO add your handling code here:
       
        String transformer_no = tnotxt.getText();
        String transformer = ttxt.getText();
        String transformer_type= ttype.getText();
        String capacity = captxt.getText();
        String elec_board_id = eleid.getText();
        String elec_board_name = elename.getText();
        try {
            
            PreparedStatement ps = tran.Getconnection().prepareStatement("insert into transformer_master (transformer_no,transformer,transformer_type,capacity ,elec_board_id,elec_board_name) values (?,?,?,?,?,?)");
            ps.setString(1,transformer_no );
            ps.setString(2,transformer );
            ps.setString(3,transformer_type  );
            ps.setString(4,capacity );
            ps.setString(5,elec_board_id);
            ps.setString(6,elec_board_name);
            ps.executeUpdate ();
           JOptionPane.showMessageDialog (null, "Data Inserted Successfully");
        ref_data();
        tnotxt.setText("");
        ttxt.setText("");
        ttype.setText("");
        captxt.setText("");
        eleid.setText("");
        elename.setText("");
        }
        catch (Exception e){           
            JOptionPane.showMessageDialog (null, "Unable to insert data: " + e.getLocalizedMessage());
        }
       
    }//GEN-LAST:event_insertbuttActionPerformed

