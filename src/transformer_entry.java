   private void disp_board_details() {
       String ele =eleid.getText();
       try
       {
           PreparedStatement ps =tran.Getconnection().prepareStatement("select elec_board_name " + " from electricity_board_master where elec_board_id=?");
           ps.setString(1,ele);
           tranrst=ps.executeQuery();
           if(tranrst.next() ){
              elename.setText(tranrst.getString("elec_board_name"));
              
         }
       }
           catch(Exception ex){
                 
                   Logger.getLogger(Consumer_entry.class.getName()).log(Level.SEVERE, null, ex);
                   }
       
      
        
    }//GEN-LAST:event_eleidKeyPressed
  private void ref_data() {
     try {
        model.setRowCount(0);
        tranrst = transtmt.executeQuery("select * from transformer_master");
        while(tranrst.next ()) {
        model.addRow(new Object[]
        {tranrst.getString("transformer_no"),tranrst.getString("transformer"),tranrst.getString("transformer_type"),tranrst.getString("capacity"),tranrst.getString("elec_board_id"),tranrst.getString("elec_board_name")});
         }
        }
         catch (Exception e){
             System.err.println(e);
         }
}      
    
           
